// Sorting Function 

function Tsort(){
    var selectedsort =document.querySelector("#sortT").value
   
    if(selectedsort === "high"){
        productData = productData.sort(function(a,b){
            return b.price - a.price
        })
    }
    if(selectedsort === "low"){
        productData = productData.sort(function(a,b){
            return a.price - b.price
        })
    }
    reLoadM(productData)
} 

   
 

